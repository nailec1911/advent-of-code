f = [
".......S.......",
"...............",
".......^.......",
"...............",
"......^.^......",
"...............",
".....^.^.^.....",
"...............",
"....^.^...^....",
"...............",
"...^.^...^.^...",
"...............",
"..^...^.....^..",
"...............",
".^.^.^.^.^...^.",
"...............",
]


f = [[0 if x == '.' else x for x in e[:-1]] for e in list(open("input.txt"))]


f[0][f[0].index('S')] = 1

res = 0
for i in range(1, len(f)):
    for j in range(len(f[0])):
        if f[i - 1][j] not in [0, '^']:
            if f[i][j] != '^' and f[i - 1][j] != '^':
                f[i][j] += f[i - 1][j]
            if f[i][j] == '^':
                f[i][j - 1] += f[i - 1][j]
                f[i][j + 1]  += f[i - 1][j]
                res += 1

print(res)
print(sum(e for e in f[-1] if e != '^'))
